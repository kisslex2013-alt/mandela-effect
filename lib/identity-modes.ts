// Архетипы Реальности (Скучные)
function getRealityArchetype(rate: number) {
  if (rate > 80) return "Образцовый Гражданин";
  if (rate > 50) return "Стандартный Наблюдатель";
  return "Субъект с Фантазией";
}

// Архетипы Изнанки (Крутые)
function getUpsideDownArchetype(rate: number) {
  if (rate > 80) return "Хранитель Исходного Кода";
  if (rate > 50) return "Сквозной Дрифтер";
  return "Глитч в Системе";
}

function getRandom(arr: string[]) {
  return arr[Math.floor(Math.random() * arr.length)];
}

